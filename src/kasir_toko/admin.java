    private void autonumber (){
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM barang ORDER BY IdBarang DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoFaktur = r.getString("IdBarang").substring(2);
                String BR = "" + (Integer.parseInt(NoFaktur)+1);
                String No1 = "";
                
                if (BR.length()== 1){
                    No1 = "00";
                } else if (BR.length() == 2){
                    No1 = "0";
                } else if (BR.length()== 3){
                    No1 = "";
                }
                
                txIDBarang.setText("BR" + No1 + BR);
            } else{
                txIDBarang.setText("BR001");
            }
            r.close();
            s.close();
        } catch (Exception e){
            System.out.println("autonumber error");
        }
    }
    

    public void loadData(){
        model.getDataVector().removeAllElements();
        
        model.fireTableDataChanged();
        
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            
            String sql = "SELECT * FROM barang";
            ResultSet r = s.executeQuery(sql);
            
            while (r.next()){
                Object[] o = new Object[6];
                o [0] = r.getString("IdBarang");
                o [1] = r.getString("namaBarang");
                o [2] = r.getString("kategori");
                o [3] = r.getString("hargaBeli");
                o [4] = r.getString("hargaJual");
                o [5] = r.getString("stok");
                
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (Exception e){
            System.out.println("Terjadi Kesalahan 1");
        }
    }
    
    public void cariData(){
        DefaultTableModel tabel = new DefaultTableModel();
        
        tabel.addColumn("IdBarang");
        tabel.addColumn("namBarang");
        tabel.addColumn("kategori");
        tabel.addColumn("hargaBeli");
        tabel.addColumn("hargaJual");
        tabel.addColumn("Stok");
        
        try {
            Connection c = koneksi.getKoneksi();
            String sql = "SELECT * FROM barang WHERE IdBarang like '%" + txCariData.getText()
                    + "%'" + "OR namaBarang like '%" + txCariData.getText() + "%'";
            Statement stat = c.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()){
                tabel.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                });
            }
            jTable2.setModel(tabel);
            loadData();
        } catch (Exception e) {
            System.out.println("Cari Data Error");
        }finally{
            
        }
    }
    
    

    public void totalBiaya(){
        int jumlahBaris = jTable1.getRowCount();
        int totalBiaya = 0;
        int jumlahBarang, hargaBarang;
        for (int i = 0; i < jumlahBaris; i++){
            jumlahBarang = Integer.parseInt(jTable1.getValueAt(i, 3).toString());
            hargaBarang = Integer.parseInt(jTable1.getValueAt(i, 4).toString());
            totalBiaya = totalBiaya + (jumlahBarang * hargaBarang);
        }
        txTotalBayar.setText(String.valueOf(totalBiaya));
        txTampil.setText("Rp " + totalBiaya + ",00");
    }
    
    private void autonumber(){
        try {
        Connection c = koneksi.getKoneksi();
        Statement s = c.createStatement();
        String sql = "SELECT * FROM penjualan ORDER BY noFaktur DESC";
        ResultSet r = s.executeQuery(sql);
        if (r.next()){
            String noFaktur = r.getString("noFaktur");
            String numericPart = noFaktur.replaceAll("[^0-9]", "");
            String TR = "" + (Integer.parseInt(numericPart) + 1);
            String No1 = "";

            if (TR.length() == 1){
                No1 = "000";
            } else if (TR.length() == 2){
                No1 = "00";
            } else if (TR.length() == 3){
                No1 = "0";
            } else if (TR.length() == 4){
                No1 = "";
            }
            txNoTransaksi.setText("ILM" + No1 + TR);
        } else {
            txNoTransaksi.setText("ILM0001");
        }
        r.close();
        s.close();
        } catch (Exception e){
            System.out.println("Kesalahan autonumber: " + e.getMessage());
        }
       }
    

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        String noTransaksi = txNoTransaksi.getText();
        String tanggal = txTanggal.getText();
        String idCustomer = txIDCustomer.getText();
        String total = txTotalBayar.getText();
        String namaCustomer = txNamaCustomer.getText();
        String pembayaran = txBayar.getText();
        String kembalian = txKembalian.getText();
        
        try {
            Connection c = koneksi.getKoneksi();
            String sql = "INSERT INTO penjualan VALUES (?, ?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, noTransaksi);
            p.setString(2, tanggal);
            p.setString(3, idCustomer);
            p.setString(4, total);
            p.setString(5, namaCustomer);
            p.executeUpdate();
            p.close();
        } catch (Exception e) {
            System.out.println("simpan penjualan erorr");
        }
        
        List<Object[]> items = new ArrayList<>();
        
        try {
            Connection c = koneksi.getKoneksi();
            int baris = jTable1.getRowCount();

            String sql = "INSERT INTO penjualanrinci(noFaktur, idBarang, namaBarang, jumlah, harga, total) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);

            for (int i = 0; i < baris; i++){
                String noFaktur = jTable1.getValueAt(i, 0).toString();
                String idBarang = jTable1.getValueAt(i, 1).toString();
                String namaBarang = jTable1.getValueAt(i, 2).toString();
                String jumlah = jTable1.getValueAt(i, 3).toString();
                String harga = jTable1.getValueAt(i, 4).toString();
                String totalItem = jTable1.getValueAt(i, 5).toString();

                p.setString(1, noFaktur);
                p.setString(2, idBarang);
                p.setString(3, namaBarang);
                p.setString(4, jumlah);
                p.setString(5, harga);
                p.setString(6, totalItem);
                p.executeUpdate();
                
                items.add(new Object[]{noFaktur, idBarang, namaBarang, jumlah, harga, totalItem});
            }
            p.close();
        } catch (Exception e){
            System.out.println("simpan penjualan rinci error" + e.getMessage());
        }
        
        try {
            StrukPDF cetakPDF = new StrukPDF();
            cetakPDF.createTransactionPDF(noTransaksi, tanggal, idCustomer, namaCustomer, total, pembayaran, kembalian, items);
        } catch (Exception e) {
            System.out.println("Gagal mencetak");
        }        
   
        clear();
        utama();
        autonumber();
        kosong();
        txTampil.setText("Rp 0");
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void txBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txBayarActionPerformed
        // TODO add your handling code here:
        int total, bayar, kembalian;
        
        total = Integer.valueOf(txTotalBayar.getText());
        bayar = Integer.valueOf(txBayar.getText());
        
        if (total > bayar) {
            JOptionPane.showMessageDialog(this, "Uang tidak mencukupi");
        } else {
            kembalian = bayar - total;
            txKembalian.setText(String.valueOf(kembalian));
        }
    }//GEN-LAST:event_txBayarActionPerformed

